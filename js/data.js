// ── Process & assign IDs ── //
let _qid = 0;
let QUESTIONS_GK = [];
let QUESTIONS_CUET = [];
let QUESTIONS_NTA = [];
let GK_TESTS = [];
let NTA_TESTS = [];
let ALL_QUESTIONS = [];
let QUESTION_MAP = {};

const Data = {
  async init() {
    _qid = 0;
    
    // Base questions
    let base_gk = _RAW_GK.map(q => ({
      ...q,
      id: `gk_${_qid++}`,
      bank: 'gk'
    }));
    
    let base_cuet = _RAW_CUET.map(q => ({
      ...q,
      id: `cuet_${_qid++}`,
      bank: 'cuet'
    }));

    let base_nta = _RAW_NTA.map(q => ({
      ...q,
      id: `nta_${_qid++}`,
      bank: 'nta'
    }));

    // Merge custom questions from store
    const customQuestions = (window.Store ? await Store.getCustomQuestions() : []);
    
    // Add custom questions or replace existing ones by ID
    const merged_gk = [...base_gk];
    const merged_cuet = [...base_cuet];
    const merged_nta = [...base_nta];
    
    customQuestions.forEach(cq => {
      if (cq.bank === 'gk') {
        const existingIdx = merged_gk.findIndex(q => q.id === cq.id);
        if (existingIdx >= 0) merged_gk[existingIdx] = cq;
        else merged_gk.push(cq);
      } else if (cq.bank === 'cuet') {
        const existingIdx = merged_cuet.findIndex(q => q.id === cq.id);
        if (existingIdx >= 0) merged_cuet[existingIdx] = cq;
        else merged_cuet.push(cq);
      } else if (cq.bank === 'nta') {
        const existingIdx = merged_nta.findIndex(q => q.id === cq.id);
        if (existingIdx >= 0) merged_nta[existingIdx] = cq;
        else merged_nta.push(cq);
      }
    });

    QUESTIONS_GK = merged_gk;
    QUESTIONS_CUET = merged_cuet;
    QUESTIONS_NTA = merged_nta;

    // ── Build GK test sets (25 questions each, mixed) ── //
    const shuffled = Utils.shuffle([...QUESTIONS_GK]);
    const tests = [];
    for (let i = 0; i < shuffled.length; i += 25) {
      tests.push(shuffled.slice(i, i + 25));
    }
    while (tests.length < 3) {
      tests.push(Utils.shuffle([...QUESTIONS_GK]).slice(0, 25));
    }
    GK_TESTS = tests;

    // ── Build NTA test sets ── //
    NTA_TESTS = [Utils.shuffle([...QUESTIONS_NTA])];

    // ── All questions combined ── //
    ALL_QUESTIONS = [...QUESTIONS_GK, ...QUESTIONS_CUET, ...QUESTIONS_NTA];

    // ── Question lookup by ID ── //
    QUESTION_MAP = {};
    ALL_QUESTIONS.forEach(q => { QUESTION_MAP[q.id] = q; });
  }
};

